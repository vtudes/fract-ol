#include "libft.h"

void	ft_putstr_ptr(char **str)
{
	int i;

	i = 0;
	if (str)
	{
		while (str[i])
		{
			ft_putstr(str[i]);
			ft_putchar('\n');
			i++;
		}
	}
}
